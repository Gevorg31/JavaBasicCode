package com.syntax.class09;

public class Array {

	public static void main(String[] args) {
		// Array is a container that can store multiple values int[]
		
		// create an array
		
		int[] b = new int[4];
		
		// store the values
		 
		b[0] = 90;
		b[1] = 85;
		b[2] = 70;
		b[3] = 100;
		
		// access values from my array
		
		System.out.println(b[2]); // 70
		System.out.println(b[1]+b[3]); //185
		
	}

}
