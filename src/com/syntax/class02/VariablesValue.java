public class VariablesValue {

	public static void main(String[] args) {
		
		byte num1=4;
		num1=6;
		num1=9;
		System.out.println(num1);
		char num2='$';
		num2='&';
		num2='@';
		System.out.println(num2);
		double num3=14.99;
		num3=15.66;
		num3=17.99;
		System.out.println(num3);
		short num4=199;
		num4=100;
		System.out.println(num4);
			
	}

}
